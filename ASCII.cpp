// Question:-   To print ASCII value of all Uppercase Alphabet

#include<iostream>
using namespace std;
int main(){
    char n;
    
    for(n='A';n<='Z';n++){
        cout << "ASCII value of: " << n << " = " << (int)n << endl;
    }
    
 return 0;
}