// Question:-  to print all natural numbers in reverse order
#include<iostream>
using namespace std;
int main(){
    int n,i;
    cout<<"Enter no whose reverse order you want to print"<<endl;
    cin>>n;
    for(i=n;i>=1;i--){
        cout<<"The numbers are :-"<<i<<endl;
    }
 return 0;
}